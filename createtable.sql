DROP TABLE IF EXISTS kickstarter;
CREATE TABLE kickstarter (
  backers_count real,
  blurb text,
  category real,
  created_at real,
  currency text,
  deadline real,
  goal real,
  launched_at real,
  ks_name text,
  pledged real,
  spotlight text,
  staff_pick text,
  current_state text,
  state_change real,
  urls text
);